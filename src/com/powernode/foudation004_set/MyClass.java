package com.powernode.foudation004_set;

public class MyClass <fdas> {
    private fdas name;

    public fdas getName() {
        return name;
    }

    public MyClass(fdas name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "MyClass{" +
                "name=" + name +
                '}';
    }
}
